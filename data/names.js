window.Game.Data.generateName = function() {
    const isMale = Math.random() > 0.45; // Slight male bias for Thai politics
    const firsts = isMale ? window.Game.Data.FIRST_NAMES_MALE : window.Game.Data.FIRST_NAMES_FEMALE;
    const first = firsts[Math.floor(Math.random() * firsts.length)];
    const last = window.Game.Data.LAST_NAMES[Math.floor(Math.random() * window.Game.Data.LAST_NAMES.length)];
    return first + " " + last;
};

// Generate unique names for a roster
window.Game.Data.generateRoster = function(count) {
    const names = new Set();
    while (names.size < count) {
        names.add(window.Game.Data.generateName());
    }
    return Array.from(names);
};
